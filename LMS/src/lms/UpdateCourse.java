    private void Button_Done_UpCourse1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_Done_UpCourse1ActionPerformed
        // TODO add your handling code here:
        String stream = ComboBox_Stream_UpSylbs.getSelectedItem().toString();
        String branch = ComboBox_Branch_UpSylbs.getSelectedItem().toString();
        String semes = ComboBox_Sem_UpSylbs.getSelectedItem().toString();
        String subj = ComboBox_Sub_UpSylbs.getSelectedItem().toString();
        
        try
        { 
            Class.forName("oracle.jdbc.driver.OracleDriver");
            Connection con = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE","SYSTEM","sagar");
            String sql = "update course set syllabus_link=? where stream=? and branch=? and sem=? and subject=?";
            PreparedStatement ps = con.prepareStatement(sql);
            File f = new File(filelocation);
            FileReader fr = new FileReader(f);
            ps.setCharacterStream(1,fr,(int)f.length());
            ps.setString(2, stream);
            ps.setString(3, branch);
            ps.setString(4, semes);
            ps.setString(5, subj);
            int i = ps.executeUpdate();
            if(i>0)
            {
                JOptionPane.showMessageDialog(null,"Syllabus Updated");
            }
            con.close();
        }
            catch(ClassNotFoundException e)
            {System.out.println(e);}
            catch(Exception e)
            {System.out.println(e);}
            this.setVisible(false);
    }//GEN-LAST:event_Button_Done_UpCourse1ActionPerformed

